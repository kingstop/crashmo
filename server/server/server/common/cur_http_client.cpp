static int OnDebug(CURL* curl, curl_infotype t, char * Data, size_t s, void* v)
{
	switch (t)
	{
	case CURLINFO_TEXT:
	{

	}
	break;
	case CURLINFO_DATA_IN:
	{
		printf("{DATA_IN}%s\n", Data);
	}
	break;
	case CURLINFO_DATA_OUT:
	{
		printf("{DATA_OUT}%s\n", Data);
	}
	break;
	case CURLINFO_HEADER_IN:
	{
		printf("{HEADER_IN}%s\n", Data);
	}
	break;
	case CURLINFO_HEADER_OUT:
	{
		printf("{HEADER_OUT}%s\n", Data);
	}
	break;
	default:
		break;
	}
	return 0;
}

static size_t OnWriteData(void* buffer, size_t size, size_t nmemb, void* lpvoid)
{
	std::string * str = dynamic_cast<std::string*>((std::string *)lpvoid);
	if (NULL == str || NULL == buffer)
	{
		return -1;
	}

	char* pData = (char*)buffer;
	str->append(pData, size * nmemb);
	return nmemb;
}


int CurlHttpClient::Post(const std::string & strUrl, const std::string & strPost, std::string & strResponse)
{
	CURLcode res;
	CURL* curl = curl_easy_init();
	if (NULL == curl)
	{
		return CURLE_FAILED_INIT;
	}

	if (_debug)
	{
		curl_easy_setopt(curl, CURLOPT_VERBOSE, 1);
		curl_easy_setopt(curl, CURLOPT_DEBUGFUNCTION, OnDebug);
	}
	curl_easy_setopt(curl, CURLOPT_URL, strUrl.c_str());
	curl_easy_setopt(curl, CURLOPT_POST, 1);
	curl_easy_setopt(curl, CURLOPT_POSTFIELDS, strPost.c_str());
	curl_easy_setopt(curl, CURLOPT_READFUNCTION, NULL);
	curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, OnWriteData);
	curl_easy_setopt(curl, CURLOPT_WRITEDATA, (void*)&strResponse);
	curl_easy_setopt(curl, CURLOPT_NOSIGNAL, 1);
	curl_easy_setopt(curl, CURLOPT_CONNECTTIMEOUT, 3);
	curl_easy_setopt(curl, CURLOPT_TIMEOUT, 3);
	res = curl_easy_perform(curl);
	curl_easy_cleanup(curl);
	return res;
}

